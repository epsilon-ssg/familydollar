package com.familydollar.corporate.core.models;

public class SocialIcon {
	
	private String iconImagePath;
	
	private String iconLinkURL;
	
	private boolean newWindow;
	
	private String iconAltText;

	public String getIconImagePath() {
		return iconImagePath;
	}

	public void setIconImagePath(String iconImagePath) {
		this.iconImagePath = iconImagePath;
	}

	public String getIconLinkURL() {
		return iconLinkURL;
	}

	public void setIconLinkURL(String iconLinkURL) {
		this.iconLinkURL = iconLinkURL;
	}

	public boolean isNewWindow() {
		return newWindow;
	}

	public void setNewWindow(boolean newWindow) {
		this.newWindow = newWindow;
	}

	public String getIconAltText() {
		return iconAltText;
	}

	public void setIconAltText(String iconAltText) {
		this.iconAltText = iconAltText;
	}
	
	

}
